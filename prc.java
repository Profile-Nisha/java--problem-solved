public class prc {
    public static void main(String args[]){
        int x = 10;
        int y = 20;

        System.out.println("minimum of the number x and y : "  + Math.min(x, y));
        System.out.println("maximum of the number x and y: " + Math.max(x, y));
        System.out.println("square of the number :" + Math.sqrt(y));
        System.out.println("power of the number x and y : " + Math.pow(x, y));
        System.out.println("average of the number x and y: " + Math.sin(y));
        System.out.println("absolute value of the number : " + Math.abs(y));
    }
    
}
